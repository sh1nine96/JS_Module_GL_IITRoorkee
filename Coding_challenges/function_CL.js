function rectangleArea(length, width){
    return length*width;
}
console.log('area of rectangle having length=10, width=5, is: ', rectangleArea(10, 5));

function squareArea(side){
    return side*side;
}
console.log('area of square having side = 5, is: ',squareArea(5));

function circleArea(radius){
    return 3.14*radius*radius;
}
console.log("area of circle having radius=5 is: ", circleArea(5));